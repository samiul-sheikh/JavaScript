var friendsAge = [16,24,30,28];
console.log(friendsAge);

// for add last element in array
friendsAge.push(22);
friendsAge.push(12);
console.log(friendsAge);

// for define array lenght
console.log(friendsAge.length);

// for remove last element in array
friendsAge.pop();
console.log(friendsAge);

// for remove first element in array
friendsAge.shift();
console.log(friendsAge);

// for add first element in array
friendsAge.unshift(5);
console.log(friendsAge);